public class Logical {
    public static void main(String[] args) {
        // LOGICAL OPERATORS
        int a = 10;
        int b = 20;
        System.out.println(a > 5 && b < 30);
        System.out.println(a < 5 || b < 30);
        System.out.println(!(a < 5));
        
       
       



    }
    
}
