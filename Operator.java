public class Operator {
    public static void main(String[] args) {
        // ASSIGNMENT OPERATORS
        int x = 5;
        x += 3; 
        System.out.println(x); 
        x -= 2;
        System.out.println(x);
        x *= 5;
        System.out.println(x);
        x /= 8;
        System.out.println(x);
        x %= 2;
        System.out.println(x);
    }
    
}
