public class Relational {
    public static void main(String[] args) {
         // COMPARISON OPERATORS
         int y = 10;
         int z = 20;
         System.out.println(y == z); 
         System.out.println(y != z);
         System.out.println(y > z);
         System.out.println(y < z);
         System.out.println(y >= z);
         System.out.println(y <= z);
    }
    
}
